public class Main {
    public static void main(String[] args) {

        double i = 3.14;
        double j = 6.42;

        System.out.println("i is " + i);
        System.out.println("j is " + j);

        double k = i / j;
        System.out.println("i / j is " + k);
        k = i * j;
        System.out.println("i * j is " + k);
        k = i - j;
        System.out.println("i - j is " + k);

    }

}
